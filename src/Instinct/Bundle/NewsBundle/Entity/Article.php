namespace Instinct\Bundle\NewsBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Instinct\Bundle\NewsBundle\Entity\Article
 *
 * @ORM\Table(name="instinct_news_article")
 * @ORM\Entity(repositoryClass="Instinct\Bundle\NewsBundle\Entity\ArticleRepository")
 */
class Article
{
    /**
     * @var integer $id
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string $title
     *
     * @ORM\Column(name="title", type="string", length=255)
     */
    private $title;

    /**
     * @var text $content
     *
     * @ORM\Column(name="content", type="text")
     */
    private $content;

    /**
     * @var \Instinct\Bundle\UserBundle\Entity\User $autor
     *
     * @ORM\ManyToOne(targetEntity="Instinct\Bundle\UserBundle\Entity\User")
     */
    private $autor;

    /**
     * @var date $date
     *
     * @ORM\Column(name="date", type="date")
     */
    private $date;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set title
     *
     * @param string $title
     */
    public function setTitle($title)
    {
        $this->title = $title;
    }

    /**
     * Get title
     *
     * @return string
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set content
     *
     * @param text $content
     */
    public function setContent($content)
    {
        $this->content = $content;
    }

    /**
     * Get content
     *
     * @return text
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * Set autor
     *
     * @param \Instinct\Bundle\UserBundle\Entity\User $autor
     */
    public function setAutor(\Instinct\Bundle\UserBundle\Entity\User $autor)
    {
        $this->autor = $autor;
    }

    /**
     * Get autor
     *
     * @return \Instinct\Bundle\UserBundle\Entity\User
     */
    public function getAutor()
    {
        return $this->autor;
    }

    /**
     * Set date
     *
     * @param date $date
     */
    public function setDate($date)
    {
        $this->date = $date;
    }

    /**
     * Get date
     *
     * @return date
     */
    public function getDate()
    {
        return $this->date;
    }
}